def get_question_categories_to_question_ids():
    all_statements_data = statements_data

    question_categories_to_question_ids = {}
    for qc in question_categories:
        question_categories_to_question_ids[qc] = []

    for d in all_statements_data:
        qc = d["question_category"]
        question_categories_to_question_ids[qc].extend(d["question_ids"])

    return question_categories_to_question_ids


def get_statements_qids_by_category():
    statements_qids_by_category = {}
    for d in statements_data:
        qc = d["question_category"]
        if qc not in statements_qids_by_category:
            statements_qids_by_category[qc] = []
        statements_qids_by_category[qc].extend(d["question_ids"])
    return statements_qids_by_category


def get_probe_setups(num_probes=3, num_questions_per_probe=3):
    question_categories_to_question_ids = get_question_categories_to_question_ids()

    probe_setups = []
    for probe_setup_idx in range(num_probes):
        probe_setup = {qc: [] for qc in question_categories_to_question_ids}

        for qc in question_categories_to_question_ids:
            question_ids = question_categories_to_question_ids[qc]

            total_questions_in_qc = len(question_ids)

            raw_idx_to_select = [probe_setup_idx * num_questions_per_probe +
                                 num_ques for num_ques in range(num_questions_per_probe)]
            raw_idx_to_select = [
                idx % total_questions_in_qc for idx in raw_idx_to_select]

            probe_setup[qc] = [question_ids[idx] for idx in raw_idx_to_select]

        probe_setups.append(probe_setup)
    return probe_setups


def get_all_probe_qids(probe_setup_id=None):
    all_probe_qids = []
    if probe_setup_id is None:
        for probe_setup in get_probe_setups():
            for qc in probe_setup:
                all_probe_qids.extend(probe_setup[qc])
    else:
        probe_setup = get_probe_setups()[probe_setup_id]
        for qc in probe_setup:
            all_probe_qids.extend(probe_setup[qc])
    return list(all_probe_qids)


